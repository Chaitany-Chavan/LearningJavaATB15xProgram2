package Ex_04_Operators;

public class Lab038_Operator_Arithmatic {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 22;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        float c = 3.0f;
        System.out.println(a / c);
        System.out.println("a+b"); // print as it as string only with used with double quotes
        System.out.println(a + b);

    }
}