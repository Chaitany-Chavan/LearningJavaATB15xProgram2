package Ex_04_Operators;

public class Lab041_Operator_Rational {
    static void main() {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        int a = 40;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int age_mamitha = 36;
        int age_pramod = 34;

        boolean result = age_pramod >= age_mamitha;
        System.out.println(result);
    }
}
